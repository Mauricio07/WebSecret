CREATE PROCEDURE SP_ADD_PRODUCTS @idRecipe int, @pack int, @idBox int, @codProd varchar(20),
	@nameProd varchar(100), @path varchar(200), @desc varchar(100), @codUpc varchar(20), @onlineName varchar(100)
AS

declare @IdProd int
SET NOCOUNT ON
BEGIN TRANSACTION
	BEGIN TRY
		-- ADD PRODUCT
		INSERT INTO PRODUCTS(CODE_PRODUCT, NAME_PRODUCT, ID_BOX, IMAGE_PRODUCT, DESCRIPTION_PRODUCT, UPC_PRODUCT, ONLINENAME_PRODUCT, DATECREATE_PRODUCT, STATUS_PRODUCT)
		VALUES(@codProd, @nameProd, @idBox, @path, @desc, @codUpc, @onlineName, GETDATE(),1);
		
		set @IdProd=(SELECT MAX(ID_PRODUCT) FROM PRODUCTS); --Id product

		-- ADD PRODUCT RECIPES
		INSERT INTO PRODUCT_RECIPIES(ID_PRODUCT, ID_RECIPE, PACK)
		VALUES(@IdProd, @idRecipe, @pack);

		SELECT @IdProd AS'ID';

		COMMIT --add
	END TRY
	BEGIN CATCH
		ROLLBACK
		SELECT 0;
	END CATCH
go

